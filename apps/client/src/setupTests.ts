// This file runs once per test file (after Jest's framework is loaded).
// Importing jest-dom adds custom matchers to `expect`, like:
//   - toBeInTheDocument()  → element exists in the rendered DOM
//   - toHaveValue('foo')   → input has a specific value
//   - toBeDisabled()       → element has the disabled attribute
//   - toHaveTextContent()  → element contains specific text
// Without this import, those matchers don't exist and calls like
// `expect(el).toBeInTheDocument()` would throw "not a function".
import '@testing-library/jest-dom';
